

/**
* add - add numbers
* @a: first
* @b: second
*
* Return: int
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - sub numbers
* @a: first
* @b: second
*
* Return: int
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiply numbers
* @a: first
* @b: second
*
* Return: int
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divide numbers
* @a: first
* @b: second
*
* Return: int
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod numbers
* @a: first
* @b: second
*
* Return: int
*/
int mod(int a, int b)
{
	return (a % b);
}
