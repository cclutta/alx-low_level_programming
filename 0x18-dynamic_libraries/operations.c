#include <stdlib.h>
#include <stdio.h>

/**
* add - add numbers
* @a: first
* @b: second
*
* Return: int
*/
int add(a, b)
{
	return (a + b);
}

/**
* sub - sub numbers
* @a: first
* @b: second
*
* Return: int
*/
int sub(a, b)
{
	return (a - b);
}

/**
* mul - multiply numbers
* @a: first
* @b: second
*
* Return: int
*/
int mul(a, b)
{
	return (a * b);
}

/**
* div - divide numbers
* @a: first
* @b: second
*
* Return: int
*/
int div(a, b)
{
	return (a / b);
}

/**
* mod - mod numbers
* @a: first
* @b: second
*
* Return: int
*/
int mod(a, b)
{
	return (a % b);
}
